// Merging two halves of the array
void merge(int arr[], int left, int mid, int right,int &compare_count, int &swap_count) {
    int n1 = mid - left + 1;
    int n2 = right - mid;
    // Create temporary arrays
    int* leftArr = new int[n1];
    int* rightArr = new int[n2];
    // Copy data to temporary arrays
    for (int i = 0; i < n1; i++)
        leftArr[i] = arr[left + i];
    for (int i = 0; i < n2; i++)
        rightArr[i] = arr[mid + 1 + i];
    // Merge the temp arrays back into arr
    int i = 0, j = 0, k = left;
    while (i < n1 && j < n2) {
        compare_count+=1;
        if (leftArr[i] <= rightArr[j]) {
            arr[k] = leftArr[i];
            i++;
        } else {
            arr[k] = rightArr[j];
            j++;
        }
        k++;
    }
    // Copy remaining elements of leftArr[]
    while (i < n1) {
        compare_count+=1;
        arr[k] = leftArr[i];
        i++;
        k++;
    }
    // Copy remaining elements of rightArr[]
    while (j < n2) {
        compare_count+=1;
        arr[k] = rightArr[j];
        j++;
        k++;
    }
    // Free dynamically allocated memory
    delete[] leftArr;
    delete[] rightArr;
}
// Recursive Merge Sort function
void mergeSort(int arr[], int left, int right,int &compare_count, int &swap_count) {
    if (left < right) {
        int mid = left + (right - left) / 2;
        // Recursively sort the two halves
        mergeSort(arr, left, mid ,compare_count,swap_count);
        mergeSort(arr, mid + 1, right,compare_count,swap_count);
        // Merge sorted halves
        merge(arr, left, mid, right,compare_count,swap_count);
    }
}


