void selection_sort(int size, int S[1000],int &compare_count, int &swap_count){   
     // finds the smallest number in the array
    for(int i=0;i < size-1;i++){
            int smallest=i;
            compare_count+=1;
            // swaps j and the smallest number
            for(int j=i+1;j < size;j++){
                if(S[j]< S[smallest]){
                smallest=j;
                swap_count+=1;
        }
    }
    //uses the swap function
    swap(S[i], S[smallest]);
    }
}
// exchange sort
void exchange_sort(int size, int S[1000],int &compare_count, int &swap_count){
    for (int i = 0; i < size-1; i++){
        // swaps if the number is smaller
        for (int j = i + 1; j < size; j++){
            if(S[i] > S[j]){
                swap_count+=1;
                //uses the swap function
                swap(S[i],S[j]);
            }
            compare_count+=1;
            }
    }

}

void bubbleSort(int arr[], int n,int &compare_count, int &swap_count) {
    for (int i = 0; i < n - 1; ++i) {
        for (int j = 0; j < n - i - 1; ++j) {
            
            if (arr[j] > arr[j + 1]) {
                // Swap arr[j] and arr[j + 1]
                int temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
                swap_count+=1;
            }
            compare_count+=1;
        }
    }
}

void insertionSort(int arr[], int n,int &compare_count, int&swap_count )
{
        for (int i = 1; i < n; ++i) {
            int key = arr[i];
            int j = i - 1;
            while (j >= 0 && arr[j] > key) {
                arr[j + 1] = arr[j];
                j = j - 1;
                swap_count+=1;
            }
            compare_count+=1;
            arr[j + 1] = key;
            }
}


void partition(int S[], int low, int high, int& pivotpoint,int &compare_count, int&swap_count){
        int i, j;
        int pivotitem;
        pivotitem=S[low];               //choose first item for pivotitem
        j = low;
        for(i = low + 1; i <= high; i++){
            if(S[i] < pivotitem){
                j++;
                swap(S[i], S[j]);
                swap_count+=1;
            }
            compare_count+=1;
            }
            pivotpoint=j;
            swap(S[low],S[pivotpoint]);
            swap_count+=1;
}

//this is the quicksort program that determines if the data needs to be sorted further
void quicksort(int S[],int low, int high, int &compare_count, int &swap_count){
        int pivotpoint;
        if(high > low){
            partition(S, low, high, pivotpoint,compare_count, swap_count);
            quicksort(S, low, pivotpoint - 1,compare_count, swap_count );
            quicksort(S, pivotpoint + 1, high,compare_count, swap_count);
        }
}

struct heap {
    int S[1000];
    int heapsize;
};







void siftdown(heap& H, int i, int& compare_count, int& swap_count) {
    int parent, largerChild;
    int siftkey;
    bool spotfound;
    siftkey = H.S[i];
    parent = i;
    spotfound = false;
    while ((2 * parent + 1 < H.heapsize) && (!spotfound)) {
        compare_count++;
        if ((2 * parent + 2 < H.heapsize) && (H.S[2 * parent + 1] < H.S[2 * parent + 2])) {
            largerChild = 2 * parent + 2;
        } else {
            largerChild = 2 * parent + 1;
        }
        compare_count++;
        if (siftkey < H.S[largerChild]) {
            swap(H.S[parent], H.S[largerChild]);
            swap_count++;
            parent = largerChild;
        } else {
            spotfound = true;
        }
    }
    H.S[parent] = siftkey;
}
int root(heap& H, int& compare_count, int& swap_count) {
    int keyout = H.S[0];
    H.S[0] = H.S[H.heapsize - 1];
    H.heapsize--;
    siftdown(H, 0, compare_count, swap_count);

    return keyout;
}
void removekeys(int n, heap& H, int S[], int& compare_count, int& swap_count) {
    for (int i = n - 1; i >= 0; i--) {
        S[i] = root(H, compare_count, swap_count);
    }
}
void makeheap(int n, heap& H, int& compare_count, int& swap_count) {
    H.heapsize = n;
    for (int i = n / 2 - 1; i >= 0; i--) { // Fixed loop range
        siftdown(H, i, compare_count, swap_count);
    }
}
void heapsort(int n, heap& H, int& compare_count, int& swap_count) {
    makeheap(n, H, compare_count, swap_count);
    removekeys(n, H, H.S, compare_count, swap_count);
}
